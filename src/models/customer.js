class Customer {
    constructor(id, name, bdate, ssn, phone, email, place, date, time, detail, status){
        this.id = id;
        this.name = name
        this.bdate = bdate;
        this.ssn = ssn;
        this.phone = phone;
        this.email = email;
        this.place = place;
        this.date = date;
        this.time = time;
        this.detail = detail;
        this.status = status
    }
}

export default Customer;